    private void prepararIngresoDatos() {
        this.jButtonEditar.setVisible(false);
        this.jButtonEliminar.setVisible(false);
        
        this.jButtonCancelar.setVisible(true);
        this.jButtonGuardar.setVisible(true);
        
        this.jTFNombreContacto.setEditable(true);
        this.jTFTelefonoContacto.setEditable(true);
        this.jTFCorreoContacto.setEditable(true);
        this.jFTFCumpleanosContacto.setEditable(true);
        
        if (this.nuevo_contacto){
            this.jTFNombreContacto.setText("");
            this.jTFTelefonoContacto.setText("");
            this.jTFCorreoContacto.setText("");
            this.jFTFCumpleanosContacto.setText("");
        }
        this.jListContactos.setEnabled(false);
    }
    
    private void clickGuardarBoton() {
        Contacto c = new Contacto();
            
        c.setNombre(this.jTFNombreContacto.getText());
        c.setNumero_telefonico(this.jTFTelefonoContacto.getText());
        c.setCorreo(this.jTFCorreoContacto.getText());
        c.setFecha_cumpleanos(this.jFTFCumpleanosContacto.getText());
        
        if (!c.getNombre().trim().equals("") && !c.getNumero_telefonico().trim().equals("")){
            if (this.nuevo_contacto){
                this.jTFNombreContacto.setText("");
                this.jTFTelefonoContacto.setText("");
                this.jTFCorreoContacto.setText("");
                this.jFTFCumpleanosContacto.setText("");
                
                this.mc.agregarContacto(c);
                this.nuevo_contacto = false;
            } else {
                this.mc.editarContacto(this.posicion_contacto_seleccionado, c);
            }
            this.cargarListaNombresContactos();

            this.jButtonCancelar.setVisible(false);
            this.jButtonGuardar.setVisible(false);

            this.jTFNombreContacto.setEditable(false);
            this.jTFTelefonoContacto.setEditable(false);
            this.jTFCorreoContacto.setEditable(false);
            this.jFTFCumpleanosContacto.setEditable(false);

            this.jListContactos.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Debes ingresar NOMBRE y NÚMERO DE TELÉFONO para guardar el contacto", "¡Espera!", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    private void clickCancelarBoton() {
        this.jButtonCancelar.setVisible(false);
        this.jButtonGuardar.setVisible(false);
        
        this.jTFNombreContacto.setEditable(false);
        this.jTFTelefonoContacto.setEditable(false);
        this.jTFCorreoContacto.setEditable(false);
        this.jFTFCumpleanosContacto.setEditable(false);
        
        if (this.nuevo_contacto) {
            this.jTFNombreContacto.setText("");
            this.jTFTelefonoContacto.setText("");
            this.jTFCorreoContacto.setText("");
            this.jFTFCumpleanosContacto.setText("");
        } else {
            this.mostrarDatosContacto();
        }
    }
    
    private void clickEliminarBoton() {
        int resp = JOptionPane.showConfirmDialog(this, "¿Esta seguro de elimminar a " + this.jTFNombreContacto.getText() + "?", "Alerta!", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        
        if (resp == 0) {
            this.mc.eliminarContacto(this.posicion_contacto_seleccionado);
            this.cargarListaNombresContactos();
        }
    }
    
    private void mostrarDatosContacto() {
        this.jButtonEditar.setVisible(true);
        this.jButtonEliminar.setVisible(true);
        
        Contacto c = this.mc.getContacto(this.posicion_contacto_seleccionado);
        
        this.jTFNombreContacto.setText(c.getNombre());
        this.jTFTelefonoContacto.setText(c.getNumero_telefonico());
        this.jTFCorreoContacto.setText(c.getCorreo());
        this.jFTFCumpleanosContacto.setText(c.getFecha_cumpleanos());
    }

