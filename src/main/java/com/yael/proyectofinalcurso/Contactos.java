    private void jButtonIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIniciarSesionActionPerformed
        String file_name = this.mu.login(this.jTFUsuario.getText(), new String(this.jPFPassword.getPassword()));
        
        if (file_name != null) {
            this.mc = new ManejadorContactos(file_name);
            this.cargarListaNombresContactos();
            
            this.jPanelLogin.setVisible(false);
            this.jMenuBar.setVisible(true);
            this.jSplitPaneAgenda.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Usuario o Contraseña incorrectos, intentelo de nuevo.", "¡Atención!", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButtonIniciarSesionActionPerformed

    private void jButtonCrearUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearUsuarioActionPerformed
        String usuario = "", password = "", confirmar_password_mensaje = "";
        boolean password_confirmado = false;
        do{
            usuario = JOptionPane.showInputDialog(this, "Ingrese el nuevo nombre de usuario", "Nuevo Usuario", JOptionPane.PLAIN_MESSAGE);
        } while (usuario != null && usuario.trim().equals(""));
        
        do{
            password = JOptionPane.showInputDialog(this, "Ingrese contraseña del usuario", "Nuevo Usuario", JOptionPane.PLAIN_MESSAGE);
        } while (password != null && password.trim().equals(""));
        
        confirmar_password_mensaje = "Confirme la contraseña del usuario.";
        do{
            confirmar_password_mensaje = JOptionPane.showInputDialog(this, confirmar_password_mensaje, "Nuevo Usuario", JOptionPane.PLAIN_MESSAGE);
            try{
                if(confirmar_password_mensaje.equals(password)){
                    password_confirmado = true;
                } else {
                    confirmar_password_mensaje = "Las contraseñas no coindicen.\nConfirme la contraseña del usuario.";
                }
            } catch (Exception e){}
            
        } while (!password_confirmado && confirmar_password_mensaje != null);
        
        if (usuario != null && password != null && password_confirmado){
            if (this.mu.agregarUsuario(usuario, password)) {
                JOptionPane.showMessageDialog(this, "Usuario creado con éxito, ahora ingrese por favor.", "¡Éxito!", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Ese usuario ya existe, intente con otro nombre.", "¡Espera!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonCrearUsuarioActionPerformed

